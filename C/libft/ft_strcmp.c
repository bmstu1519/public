#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	int		i;

	i = 0;
	if (ft_strlen(s1) == 0 && ft_strlen(s2) != 0)
		return (-1);
	if (ft_strlen(s1) != 0 && ft_strlen(s2) == 0)
		return (1);
	while (s1[i] || s2[i])
	{
		if (s1[i] != s2[i])
			return ((unsigned char)s1[i] - (unsigned char)s2[i]);
		i++;
	}
	return (0);
}
